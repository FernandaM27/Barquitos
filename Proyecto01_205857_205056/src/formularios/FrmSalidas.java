    private void cargarTabla() {
        List<Salida> salidas = salidasDAO.consultar(txtBuscar.getText());
        if (salidas != null) {
            DefaultTableModel modelo = (DefaultTableModel) tblSalidas.getModel();
            modelo.setRowCount(0);
            for (Salida c : salidas) {
                modelo.addRow(c.toArray());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Sin resultados.",
                    "InformaciÃ³n", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    private void cargarTablaID(int id) {
        Salida salida = salidasDAO.consultar(id);
        DefaultTableModel modelo = (DefaultTableModel) tblSalidas.getModel();
        modelo.setRowCount(0);
        if (salida != null) {
            modelo.addRow(salida.toArray());
        } else {
            JOptionPane.showMessageDialog(this, "No existe ninguna saldia con ese ID", "InformaciÃ³n", JOptionPane.ERROR_MESSAGE);
        }
    }


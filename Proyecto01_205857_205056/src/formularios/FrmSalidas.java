    private boolean validar(){
        if(this.dtpSalidas.datePicker.getText().isEmpty() ||this.dtpSalidas.timePicker.getText().isEmpty()|| this.txtDestino.getText().isEmpty() || this.cboxBarcos.getSelectedIndex()==0 ){
           
            return false;
        }
        return true;
    }
    
    private void guardar() {
        Timestamp t = this.obtenerFecha();
        String destino = this.txtDestino.getText();
        Barco barco = (Barco) this.cboxBarcos.getSelectedItem();
        Salida salida = new Salida(t, destino, barco);
        if (salidasDAO.guardar(salida)) {
            JOptionPane.showMessageDialog(this, "Se ha guadado con Ã©xito");
            this.limpiar();
        } else {
            JOptionPane.showMessageDialog(this, "No se ha guadado","Error!!",JOptionPane.ERROR_MESSAGE);
        }
    }

    private void actualizar() {
        Integer id= Integer.parseInt(txtIdSalida.getText());
        Timestamp t = this.obtenerFecha();
        String destino = this.txtDestino.getText();
        Barco barco = (Barco) this.cboxBarcos.getSelectedItem();
        Salida salida = new Salida(id,t, destino, barco);
        if (salidasDAO.actualizar(salida)) {
            JOptionPane.showMessageDialog(this, "Se ha actualizado con Ã©xito");
            this.limpiar();
        } else {
            JOptionPane.showMessageDialog(this, "No se ha guadado","Error!!",JOptionPane.ERROR_MESSAGE);
        }
    }

    private void eliminar(){
        int fila = this.tblSalidas.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione una salida.",
                    "PrecauciÃ³n", JOptionPane.WARNING_MESSAGE);
        } else {
            DefaultTableModel modelo = (DefaultTableModel) this.tblSalidas.getModel();
            Integer idSalida = (Integer) modelo.getValueAt(fila, 0);
            boolean eliminado = salidasDAO.eliminar(idSalida);
            if (eliminado == true) {
                JOptionPane.showMessageDialog(this, "Salida eliminada correctamente.",
                        "InformaciÃ³n", JOptionPane.INFORMATION_MESSAGE);
                this.cargarTabla();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar la salida.",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
    private void cagarSalida(){
        int fila = this.tblSalidas.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione una salida .",
                    "PrecauciÃ³n", JOptionPane.WARNING_MESSAGE);
        } else {
            DefaultTableModel modelo = (DefaultTableModel) this.tblSalidas.getModel();
            Integer idSalida = (Integer) modelo.getValueAt(fila, 0);
            Salida salida = salidasDAO.consultar(idSalida);
            if (salida != null) {
                txtIdSalida.setText(String.valueOf(salida.getIdSalida()));
                this.dtpSalidas.setDateTimePermissive(salida.getFecha().toLocalDateTime());
                this.txtDestino.setText(salida.getDestino());
                this.cboxBarcos.setSelectedItem(salida.getBarco());
            } else {
                JOptionPane.showMessageDialog(this, "No se encontrÃ³ el animal.",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        }        
    }
    
